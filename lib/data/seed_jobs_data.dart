JobDocument _job({
  required String id,
  required String title,
  required String category,
  required String industry,
  required String experienceLevel,
  required String description,
  required List<JobSkillItem> technicalSkills,
  required List<JobSkillItem> softSkills,
  required List<JobSkillItem> tools,
  EducationRequirement? education,
  ExperienceRequirement? experience,
  SalaryInfo? salary,
  required DateTime now,
}) {
  final total = technicalSkills.length + softSkills.length + tools.length;
  final all = [...technicalSkills, ...softSkills, ...tools];
  final avg = all.isEmpty ? 0.0 : all.fold<int>(0, (a, s) => a + s.requiredLevel) / all.length;
  return JobDocument(
    id: id,
    jobId: id,
    title: title,
    category: category,
    industry: industry,
    experienceLevel: experienceLevel,
    description: description,
    technicalSkills: technicalSkills,
    softSkills: softSkills,
    tools: tools,
    certifications: [],
    education: education ?? const EducationRequirement(),
    experience: experience ?? const ExperienceRequirement(),
    salary: salary ?? const SalaryInfo(),
    createdAt: now,
    updatedAt: now,
    isActive: true,
    totalSkillsCount: total,
    averageRequiredLevel: avg,
  );
}
