/// Builds a JobSkillItem with defaults.
JobSkillItem skill(String name, int level, String priority, int weight, [String category = '']) {
  return JobSkillItem(
    name: name,
    requiredLevel: level.clamp(0, 100),
    priority: priority,
    weight: weight.clamp(1, 10),
    category: category,
  );
}

